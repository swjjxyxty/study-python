# integer
a = 100
b = -100
print("a=", a)
print("b=", b)
# string

normalString = "Hello"
print("normal string=", normalString)
specialString1 = "I'am Ok!"
print("special string1 = ", specialString1)
specialString2 = 'I\'am "Ok"!'
