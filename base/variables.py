# boolean

trueBoolean = True
print("true boolean = ", trueBoolean)
falseBoolean = False
print("false boolean = ", falseBoolean)

# and
andResult = trueBoolean and falseBoolean
print("and result=", andResult)
andResult = trueBoolean & falseBoolean
print("and result=", andResult)
# or
orResult = trueBoolean or falseBoolean
print("or result=", orResult)
orResult = trueBoolean | falseBoolean
print("or result=", orResult)

# not
notResult = not falseBoolean
print("not result=", notResult)

# none
noneVar = None
print(noneVar is None)

# constant

PI = 3.14

